test_lvx128_3:
  # Test unaligned address (should mask to 16-byte boundary)
  #_ MEMORY_IN 0x0000000010001000 [F0, F1, F2, F3, F4, F5, F6, F7, F8, F9, FA, FB, FC, FD, FE, FF]
  #_ REGISTER_IN r4 0x0000000010001000
  li r5, 13  # unaligned
  lvx128 v3, r4, r5
  blr
  #_ REGISTER_OUT r4 0x0000000010001000
  #_ REGISTER_OUT r5 13
  # Should load from aligned address 0x10001000
  #_ REGISTER_OUT v3 [0000F100, F200F300, F400F500, F600F700]

