test_negx_1:
  #_ REGISTER_IN r4 0x0000000000001000
  neg. r3, r4
  mfcr r12
  blr
  #_ REGISTER_OUT r3 0xFFFFFFFFFFFFF000
  #_ REGISTER_OUT r12 0x0000000080000000

test_negx_1_constant:
  li r4, 0x1000
  neg. r3, r4
  mfcr r12
  blr
  #_ REGISTER_OUT r3 0xFFFFFFFFFFFFF000
  #_ REGISTER_OUT r12 0x0000000080000000

test_negx_negative:
  #_ REGISTER_IN r4 0xFFFFFFFFFFFFFFFF
  neg. r3, r4
  mfcr r12
  blr
  #_ REGISTER_OUT r3 0x0000000000000001
  #_ REGISTER_OUT r12 0x0000000040000000

test_negx_zero:
  #_ REGISTER_IN r4 0x0000000000000000
  neg. r3, r4
  mfcr r12
  blr
  #_ REGISTER_OUT r3 0x0000000000000000
  #_ REGISTER_OUT r12 0x0000000020000000

